-- | Submits an invocation request to AWS Lambda. Upon receiving the request,
-- Lambda executes the specified function asynchronously. To see the logs
-- generated by the Lambda function execution, see the CloudWatch logs
-- console. This operation requires permission for the lambda:InvokeAsync
-- action.
module Network.AWS.Lambda.InvokeAsync
    (
    -- * Request
      InvokeAsync
    -- ** Request constructor
    , invokeAsync
    -- ** Request lenses
    , iaFunctionName
    , iaInvokeArgs

    -- * Response
    , InvokeAsyncResponse
    -- ** Response constructor
    , invokeAsyncResponse
    -- ** Response lenses
    , iarStatus
    ) where

import Network.AWS.Prelude
import Network.AWS.Request
import Network.AWS.Lambda.Types
import qualified GHC.Exts

data InvokeAsync = InvokeAsync
    { _iaFunctionName :: Text
    , _iaInvokeArgs   :: Base64
    } deriving (Eq, Show, Generic)

-- | 'InvokeAsync' constructor.
--
-- The fields accessible through corresponding lenses are:
--
-- * 'iaFunctionName' @::@ 'Text'
--
-- * 'iaInvokeArgs' @::@ 'Base64'
--
invokeAsync :: Text -- ^ 'iaFunctionName'
            -> Base64 -- ^ 'iaInvokeArgs'
            -> InvokeAsync
invokeAsync p1 p2 = InvokeAsync
    { _iaFunctionName = p1
    , _iaInvokeArgs   = p2
    }

-- | The Lambda function name.
iaFunctionName :: Lens' InvokeAsync Text
iaFunctionName = lens _iaFunctionName (\s a -> s { _iaFunctionName = a })

-- | JSON that you want to provide to your Lambda function as input.
iaInvokeArgs :: Lens' InvokeAsync Base64
iaInvokeArgs = lens _iaInvokeArgs (\s a -> s { _iaInvokeArgs = a })

instance ToPath InvokeAsync where
    toPath InvokeAsync{..} = mconcat
        [ "/2014-11-13/functions/"
        , toText _iaFunctionName
        , "/invoke-async/"
        ]

instance ToQuery InvokeAsync where
    toQuery = const mempty

instance ToHeaders InvokeAsync

instance ToBody InvokeAsync where
    toBody = toBody . encode . _iaInvokeArgs

newtype InvokeAsyncResponse = InvokeAsyncResponse
    { _iarStatus :: Maybe Int
    } deriving (Eq, Ord, Show, Generic)

-- | 'InvokeAsyncResponse' constructor.
--
-- The fields accessible through corresponding lenses are:
--
-- * 'iarStatus' @::@ 'Maybe' 'Int'
--
invokeAsyncResponse :: InvokeAsyncResponse
invokeAsyncResponse = InvokeAsyncResponse
    { _iarStatus = Nothing
    }

-- | It will be 202 upon success.
iarStatus :: Lens' InvokeAsyncResponse (Maybe Int)
iarStatus = lens _iarStatus (\s a -> s { _iarStatus = a })

instance AWSRequest InvokeAsync where
    type Sv InvokeAsync = Lambda
    type Rs InvokeAsync = InvokeAsyncResponse

    request  = post
    response = jsonResponse $ \h o -> InvokeAsyncResponse
        <$> o .: "Status"
